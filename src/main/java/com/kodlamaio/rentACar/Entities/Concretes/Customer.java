package com.kodlamaio.rentACar.Entities.Concretes;



public class Customer {
	
	
	 private int Id;
	 private String name;
	 private String age;
	 
	 
	 
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	 
	 
}
